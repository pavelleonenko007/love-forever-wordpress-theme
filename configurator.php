function wtw_info_message()
{
    $message = '';
    if (isset($_GET['status'])) {
        if ($_GET['status'] === 'uploaded_data') {
            $message = __('Data uploaded successfully', 'wtw-translate');
        } elseif ($_GET['status'] === 'uploaded_media') {
            $message = __('Media files uploaded successfully', 'wtw-translate');
        }
    }
    return '<b>' . $message . '</b>';
}
